package com.chainsys.novbatch.loops;

public class Addition {
	 public static void main(String s[])
	    {
	        int a = 4;//This line a value is a=4
	        a++;
	        a += a;//This line a value is a=a+a a=5+5=10
	        --a;//This line a value is a=9
	        a = 7 + a;//This line a value is a=7+a a=7+9 a=16
	        a *= a;//This line a value is a=a*a a=16*16 a=256
	        a -= 3;//This line a value is a=a-3 a=256-3 a=253

	        System.out.println(" a = " + a );
	    }

}
